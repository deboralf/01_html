def calcularTemperaturaK():
    c = float ( input ("Digite a temperatura em Celsius: "))
    k = c + 273.15
    print (f"({c}) = ({k})\n\n")

calcularTemperaturaK()

def calcularTemperaturaF():
    c = float ( input ("Digite a temperatura em Celsius: "))
    f = (c * 1.8) + 32
    print (f"({c}) = ({f}) \n\n")

calcularTemperaturaF()

